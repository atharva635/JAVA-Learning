public class unfixedcolumn {
    public static void main(String[] args) {
        
        int [][] arr = {
            {2,34 ,343},
            {34, 54},
            {23, 5, 656, 67}

        };
        for (int i=0;i<arr.length;i++){
            for (int j=0;j<arr[i].length;j++){
                System.out.print(arr[i][j]+" ");
            }
            System.out.println();
            
        }

    }
}
